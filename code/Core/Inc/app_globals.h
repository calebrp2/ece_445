/* =========================================================================
 * VOLTAGE SCALING
 * actual_V = 13.200 * adc_V - 23.40
 * Calibrated: count=1862 (adc_mv=1500) → 0 V  (measured 2026-04-28)
 * ========================================================================= */
#define VSCALE_SLOPE_PPM   13200L
#define VSCALE_OFFSET_MV   -23275L

/* =========================================================================
 * CURRENT SCALING
 * I_mA = count * 0.02626
 * ========================================================================= */
#define CURR_SCALE_MA_PER_COUNT  0.02626f

static inline float count_to_current_ma(uint16_t count)
{
    return (float)count * CURR_SCALE_MA_PER_COUNT;
}

static inline int32_t count_to_actual_mv(uint16_t count)
{
    int32_t adc_mv = (int32_t)((uint32_t)count * ADC_VREF_MV / ADC_MAX_COUNT);
    return adc_mv * (int32_t)VSCALE_SLOPE_PPM / 1000L + VSCALE_OFFSET_MV;
}

static inline void fmt_mv(char *buf, size_t n, int32_t mv)
{
    int32_t amv = mv < 0 ? -mv : mv;
    snprintf(buf, n, "%s%ld.%ldV",
             mv < 0 ? "-" : "",
             (long)(amv / 1000), (long)(amv % 1000 / 100));
}

